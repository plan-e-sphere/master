<h1>Présentation de Planesphere</h1>
<br/>
Planesphere est une application gratuite qui facilite l’organisation d'évènements festifs (anniversaires, soirées étudiantes, etc.). Il suffit pour l'organisateur de créer un évènement et ses invités pourront ensuite participer librement aux courses et proposer un covoiturage ! 
</br></br>
Cette jeune Startup a été créée en 2016, née d'un constat : lorsqu'on veut organiser des soirées, on le fait souvent de manière laborieuse avec plusieurs outils de communication (réseaux sociaux, téléphone, mail etc.) et les informations vont dans tous les sens ou se perdent.
</br>
</br>
Organiser un évènement pose trois questions :</br></br>
1- Combien d'invités assisteront à l'évènement ?</br></br>
2- Qui collecte l'argent, fait les achats et les transporte?</br></br>
3- Comment se rendre au point de rendez-vous?</br></br>
Planesphere est l'outil idéal pour régler ces problèmes. Grâce à son côté "tout en un", il offre un gain de temps et de confort pour l’organisation d'évènements festifs en simplifiant la gestion des invitations et en déchargeant l'instigateur du projet des nombreux relais entre participants. Une plateforme unique pour centraliser les informations!
</br></br>
Pour toutes demandes d'informations ou suggestions, n'hésitez pas à nous contacter sur contact@plan-e-sphere.fr.
